    private void comboBookcatMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_comboBookcatMousePressed
        // TODO add your handling code here:
        comboBookcat.removeAllItems();
        Transaction trans = null;
        Session sess = HibernateUtil.getSessionFactory().openSession();
        trans = sess.beginTransaction();
        SQLQuery query = sess.createSQLQuery("select category_name from book_category");
        List ls = query.list();
        for(Object cat : ls){
           comboBookcat.addItem((String) cat);
        }
    }//GEN-LAST:event_comboBookcatMousePressed


