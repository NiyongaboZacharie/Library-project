    private void SearchComboMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SearchComboMousePressed
        // TODO add your handling code here:
        SearchCombo.removeAllItems();
        Transaction trans = null;
        Session sess = HibernateUtil.getSessionFactory().openSession();
        trans = sess.beginTransaction();
        SQLQuery query = sess.createSQLQuery("select category_name from Book_category");
        List ls = query.list();
        for(Object cat : ls){
            SearchCombo.addItem((String) cat);
        }
    }//GEN-LAST:event_SearchComboMousePressed

    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
        Operation op= new Operation();
        op.setReportNo(Integer.parseInt(Txtreport.getText()));
        op.setOptionName(CheckIn.isSelected()?"CheckIn":"CheckOut");
        op.setBookName(SearchCombo.getSelectedItem().toString());
        op.setFromDate(new Date(TxtDchooser.getDate().getTime()));
        op.setToDate(new Date(TxtDateChooser.getDate().getTime()));
        OperationDao odao = new OperationDao();
        odao.SaveOperation(op);
        JOptionPane.showMessageDialog(this, "Data saved Successfully!!");
        populate_table();
        
        
    }//GEN-LAST:event_SearchBtnActionPerformed

